            <h1>Digital Marketing FAQs</h1>
        </div>
        <button class="accordion">What is digital marketing?
        </button>
        <div class="panel">
            <p>By definition digital marketing (aka online marketing) promotes a client’s brand, products and services
                via
                the internet or other digital channels. A digital marketing campaign is typically delivered via an
                electronic device, such as computer, tablet, phone or other medium, and leverages one or more channels,
                such
                as social media, search engines, websites, email and others, as chosen by the digital marketer based on
                your
                goals, to raise brand awareness amongst potential and current customers.</p>
        </div>

        <button class="accordion">What does a digital marketer do?</button>
        <div class="panel">
            <p>A digital marketer finds creative solutions to drive brand awareness and lead generation via free or paid
                digital channels, including email, search engines, social media, the company’s website and blog. The
                exact
                mix will depend on the client’s specific goals. The digital marketer focuses on relevant KPIs (key
                performance indicators) to measure performance and maximize revenue potential. Some companies have
                in-house
                digital marketers whilst others prefer to outsource, seeking different skills in agencies or
                freelancers,
                depending on their channel mix.</p>
        </div>

        <button class="accordion">How do digital campaigns work?</button>
        <div class="panel">
            <p>A well-planned and executed digital marketing campaign will target the right prospects for your products
                or
                services at the right time, which means they are more likely to buy or book what you offer. It’s more
                flexible, efficient, and cost-effective than many traditional marketing methods and it allows you to
                compete
                with the big guns in your industry even if you are only a small player. It also provides detailed
                analytics
                so you can measure everything from the number of visitors, to how long they spent on your page, whether
                or
                not they made a sale and so much more, including cost per click and ROI (return on investment)</p>
        </div>
        <button class="accordion">Why is digital marketing important?</button>
        <div class="panel">
            <p>
                Nowadays, it’s no longer a case of IF you have a digital presence but HOW good you are at making the
                most of
                your digital presence so you can be, and stay, competitive. That’s where digital marketing plays a
                critical
                role. Consumers are becoming increasingly sophisticated. They have higher expectations when it comes to
                the
                brands they engage with and the many ways they can be reached, such as video, email, search engines,
                social
                media.


            </p>
        </div>
        <button class="accordion">What are the different types of digital marketing?</button>
        <div class="panel">
            <p>
                You’ll hear a lot of jargon and sometimes a lot of acronyms associated with digital marketing. The most
                common types of digital marketing include Social Media Management (SMM), Search Engine Optimization
                (SEO),
                Search Engine Marketing (SEM), Pay-per-Click (PPC) Marketing, Email Marketing, Website Design and
                Maintenance, Content Marketing and Sponsored Content, Affiliate Marketing, Native Advertising, Inbound
                Marketing, and Online PR. A good digital marketer will recommend the best mix for your business based on
                your specific goals.
            </p>
        </div>
        <button class="accordion">What are the benefits of digital marketing?
        </button>
        <div class="panel">
            <p>
                Digital marketing is a must for every business. If you need customers (and who doesn’t?), you need to do
                online marketing. One of its key benefits is that it provides a cost-effective way to reach a huge
                audience
                while simultaneously targeting a specific group within that audience (reach and accuracy). It also
                allows
                you to personalize the delivery channel and message to communicate more quickly and effectively with
                your
                customers for short- and long-term relationship building.

            </p>
        </div>
    </div>




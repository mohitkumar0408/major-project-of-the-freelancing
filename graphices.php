    <h1>
        graphic design faqs
    </h1>
</div>
    </div>
    <button class="accordion">What is graphic design?
    </button>
    <div class="panel">
        <p>Put simply, graphic design is the art and craft of creating visual content that communicates a concept, an
            idea or a brand message to the public. Logos, artworks, drawings, illustrations, cards, emails and a whole
            paraphernalia of designs are all around us. You can see them in print and digital media, in shops,
            restaurants and cafes, on billboards, books and magazines, in the apps we use, the sites we visit and the
            physical and digital products we buy. In fact, graphic design is a type of communication medium which uses
            visual means to convey a message. Designers use different types of physical materials or software to combine
            images, graphics and text as the main forms of expressing this message. Graphic design is used to sell, to
            build brand identity or to move people towards specific actions. It is also a form of art but ultimately,
            the different elements of the graphic representation influence our perceptions and emotions. There are
            different types of graphic design such as ‘visual identity’ which deals with the visual elements of the
            brand via shapes, colors and images (e.g. logo design, typography, brand style guides) and ‘marketing and
            advertising’ which is used directly to generate leads and sales via print (billboards, brochures, flyers,
            print ads) or digital (social media posts, banners, videos). There are many other types such as website
            design, industrial and product design, fashion design, book and illustrations, motion graphics design used
            for example by streamers or in gaming design and many others.</p>
    </div>

    <button class="accordion">How to hire top graphic designers?</button>
    <div class="panel">
        <p>A captivating visual presence is very important whether you’re a business or a non-commercial entity. Your
            potential audiences are already forming an opinion and deciding whether to interact with you based on what
            they see, long before you’ve had a chance to say or write something. So it’s imperative to carefully select
            the right freelancer to meet your needs and keep you within budget. There are many different areas in which
            graphic designers specialize so if you want to hire the right talent to meet the requirements of your
            particular project, here are some easy to follow tips and tricks. - Always research their portfolio on
            Fiverr and ask for more examples if necessary; - Carefully think about what your style and preferences are
            so you know what you like, what you don’t like and what you actually want (colors, graphics, images, etc); -
            Write a clear brief - depending on the size of the project your brief can be very short or contain a lot of
            detail. What’s important is to be clear on the important points; - Define a budget and be clear to yourself
            and the freelancer how far you can stretch it; - Form a clear agreement on deadlines and revisions and
            respect the work of the designer - if you are clear on the above points then there shouldn’t be any
            surprises; - Think long-term - a good designer will be able to help you holistically and develop an evolving
            long-term vision for your product or service.</p>
    </div>

    <button class="accordion">How much does it cost to hire a graphic designer</button>
    <div class="panel">
        <p>There is no simple answer to this question as graphic design is not a commodity or a product that has an
            exact way to be measured or priced. The great thing about Fiverr is that you can find a freelance graphic
            designer for any budget, starting from just $5 per gig and going up to hundreds or thousands of dollars for
            more complex and time and resource consuming requirements. However, there are a number of factors that will
            influence the final price of the project such as the level of experience of the seller (from novices to Top
            Rated and Pro Sellers), the number or service options included in the gig, delivery times, number of
            revisions and whether any extras might need to be added at some point. In fact, revisions can be a very
            tricky area for graphic design projects so it’s key for you to have very clear requirements to start with
            and also to agree with the seller what their output will be (e.g.how many initial versions they will offer)
            to avoid any misunderstandings or unwanted surprises on both sides. A more experienced designer will charge
            more, however, they can also help you define your requirements and save time (and money) in the long-run by
            keeping you on track for your goals. Alternatively, a new freelancer who’s perhaps less experienced or
            trying to build their reputation will be priced more competitively but might not have the skills or
            professional maturity of a seasoned creative director.</p>
    </div>
    <button class="accordion">How do I write a good creative design brief?</button>
    <div class="panel">
        <p>
            The creative brief is a key document that essentially defines the objectives, scope and key milestones of a
            design project. It gives the required information to the freelance graphic designer on what needs to be
            done, who the target audience will be, what key message(s) need to be communicated, what deliverables are
            expected, by when (deadlines and milestones). The creative brief is basically the blueprint that you’ll need
            to agree with your internal stakeholders, business partners or colleagues (or if you are a one-person-show -
            find a friend or someone you trust to use as a sounding board) and then present to the freelancer to guide,
            inspire them and ensure they deliver the best possible creative results. Here are some universal tips for
            writing a good creative brief that equally apply to most: - Start by answering the ‘what’ (your goal is) and
            ‘who’ (your audience is); - Be clear, concise and to the point - less is more in this case so avoid being
            too prescriptive and don’t elaborate too much; - Set realistic deadlines and factor in the time for feedback
            and revisions; - Ask for help - from your team, colleagues stakeholders; - Define what success will look
            like so both you and the designer can measure the results in the end (and stick to your initial
            definitions). Once you have all this, keep it as a template and use for future briefs as it’ll save you time
            and will ensure you can build long-term relationships with the graphic designers you work with.
        </p>
    </div>
    <button class="accordion">What’s the best way to hire a designer in less than 48 hours?</button>
    <div class="panel">
        <p>
            Finding a good graphic designer is very important for the way your business and brand is presented to your
            target audience so you need to be very careful in who you put your trust in. You will need to get to grips
            with some graphic design 101’s in order to become better at selection. Having said that, we know that
            sometimes there are business emergencies and you might find yourself in a situation when you need to hire a
            professional logo designer, illustration artist, front-end/UX or web designer or any type of freelance
            graphic designer in a hurry. The good news is that Fiverr can help you do that even if you only have 48
            hours or less to complete your project. We have expert freelancers from around the globe, working 24/7
            waiting to satisfy all your needs. Here’s what to do: - Publish a buyer request so you can reach a big
